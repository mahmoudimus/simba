"""Render BENCHMARKS.md (+ a terminal table) from the append-only results log.

``write_leaderboard`` reads the JSONL, groups runs by ``(dataset, split)``, and
diffs the latest two runs per group. The markdown file is derived state — the
JSONL is the source of truth, so it carries a "do not edit by hand" banner.
"""

from __future__ import annotations

import datetime
import typing

import simba.eval.bench_results as bench_results

if typing.TYPE_CHECKING:
    import pathlib

_RECALL_METRICS = ("recall@1", "recall@3", "recall@5", "recall@10", "mrr")
_QA_METRICS = ("accuracy",)

_GENERATED_NOTE = "<!-- Generated by `simba eval leaderboard`. Do not edit by hand. -->"


def _delta_str(current: float, prev: float | None) -> str:
    """Format ``'+0.012'`` / ``'-0.005'`` / ``''`` for the delta column."""
    if prev is None:
        return ""
    d = current - prev
    return f"+{d:.3f}" if d > 0 else f"{d:.3f}"


def _iso(timestamp: object) -> str:
    """Best-effort ISO-8601 string for a run timestamp."""
    try:
        ts = float(timestamp)  # type: ignore[arg-type]
    except (TypeError, ValueError):
        return ""
    return datetime.datetime.fromtimestamp(ts, tz=datetime.UTC).strftime(
        "%Y-%m-%dT%H:%M:%SZ"
    )


def _sha(record: dict[str, object]) -> str:
    return str(record.get("git_sha", "") or "")[:7]


def _overall(record: dict[str, object], kind: str) -> dict[str, object]:
    block = record.get(kind)
    if not isinstance(block, dict):
        return {}
    overall = block.get("overall")
    return overall if isinstance(overall, dict) else {}


def _metric_table(
    latest: dict[str, object],
    prev: dict[str, object] | None,
    metrics: tuple[str, ...],
    kind: str,
) -> list[str]:
    cur = _overall(latest, kind)
    prv = _overall(prev, kind) if prev is not None else {}
    if not cur:
        return []
    rows = [
        "| Metric | Latest | Delta | Previous |",
        "|---|---|---|---|",
    ]
    for m in metrics:
        if m not in cur:
            continue
        c = float(cur[m])  # type: ignore[arg-type]
        p = prv.get(m)
        p_val = float(p) if p is not None else None  # type: ignore[arg-type]
        delta = _delta_str(c, p_val)
        prev_cell = f"{p_val:.3f}" if p_val is not None else "—"
        rows.append(f"| {m} | {c:.3f} | {delta} | {prev_cell} |")
    return rows


def render_markdown(
    groups: dict[str, tuple[dict[str, object], dict[str, object] | None]],
) -> str:
    """Render a full BENCHMARKS.md string from ``latest_two_by_group`` output."""
    lines = ["# Benchmark Results", "", _GENERATED_NOTE, ""]
    for key in sorted(groups):
        latest, prev = groups[key]
        lines.append(f"## {key}")
        lines.append("")
        cur_sha = _sha(latest)
        prev_sha = _sha(prev) if prev is not None else ""
        provenance = f"Latest: `{cur_sha}`"
        if prev is not None:
            provenance += f" — Previous: `{prev_sha}`"
        lines.append(f"_{provenance}_")
        lines.append("")

        recall_rows = _metric_table(latest, prev, _RECALL_METRICS, "recall")
        lines.extend(recall_rows)
        lines.append("")

        qa_rows = _metric_table(latest, prev, _QA_METRICS, "qa")
        if qa_rows:
            lines.append("### QA")
            lines.append("")
            lines.extend(qa_rows)
            lines.append("")

        iso = _iso(latest.get("timestamp"))
        if iso:
            lines.append(f"_Run at {iso}._")
            lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def render_stdout(
    groups: dict[str, tuple[dict[str, object], dict[str, object] | None]],
) -> str:
    """Render the compact terminal table."""
    if not groups:
        return "(no results)"
    out: list[str] = []
    for key in sorted(groups):
        latest, prev = groups[key]
        out.append(key)
        cur = _overall(latest, "recall")
        prv = _overall(prev, "recall") if prev is not None else {}
        for m in _RECALL_METRICS:
            if m not in cur:
                continue
            c = float(cur[m])  # type: ignore[arg-type]
            p = prv.get(m)
            p_val = float(p) if p is not None else None  # type: ignore[arg-type]
            delta = _delta_str(c, p_val)
            out.append(f"  {m:<10} {c:.3f}  {delta}")
        qa = _overall(latest, "qa")
        for m in _QA_METRICS:
            if m in qa:
                out.append(f"  {m:<10} {float(qa[m]):.3f}")  # type: ignore[arg-type]
        out.append("")
    return "\n".join(out).rstrip()


def write_leaderboard(
    results_path: pathlib.Path,
    output_path: pathlib.Path,
) -> str:
    """Load results, compute groups, write ``output_path``, return the markdown."""
    records = bench_results.load_results(results_path)
    groups = bench_results.latest_two_by_group(records)
    md = render_markdown(groups)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(md)
    return md
